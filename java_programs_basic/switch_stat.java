public class switch_stat {

    public static void main(String[] args){

        int tuesday=2;

        switch (tuesday){
            case 1:
                System.out.println("Monday");
                break;
            case 2:
                System.out.println("Tuesday");
                break;
            case 3:
                System.out.println("Wednesday");
                break;
            default:
                System.out.println("Invalid day");
        }
    }
    
}
