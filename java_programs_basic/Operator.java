public class Operator {

    public static void main(String[] args){
          
         int a = 20;
         int b =10;


         //Arithmatic operators 

         System.out.println("Sum of a and b is:" + (a+b));      //brackets are important 
         System.out.println("diff of a and b is :"+ (a-b));
         System.out.println("multiplication of a and b is:" + (a*b));
         System.out.println("division of a and b is:" + (a/b));
         System.out.println("modulus of a and b is :" + (a%b));


         //Relational Operator
         
         System.out.println("Greater than : " + (a > b));
         System.out.println("Less than:" + (a<b));
         System.out.println("Equal to :" + (a==b));
         System.out.println("not equals to:" + (a!=b));
         System.out.println("greater than equals to:" + (a>=b));
         System.out.println("less than equals to:" + (a<=b));


         //logical operators

         int c =5;

         System.out.println("logical and operator:" + ((a>b) && (b>c)));
         System.out.println("logical or operator:" + ((a<b) || (b>c)));
         System.out.println("logical not operator:" + !(a>b));


         // Assignment operators


         



         





    }
    
}
